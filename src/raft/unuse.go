// Kill
// the tester doesn't halt goroutines created by Raft after each test,
// but it does call the Kill() method. your code can use killed() to
// check whether Kill() has been called. the use of atomic avoids the
// need for a lock.
//
// the issue is that long-running goroutines use memory and may chew
// up CPU time, perhaps causing later tests to fail and generating
// confusing debug output. any goroutine with a long-running loop
// should call killed() to check whether it should stop.
func (rf *Raft) Kill() {
	atomic.StoreInt32(&rf.dead, 1)
	rf.mu.RLock()
	defer rf.mu.RUnlock()
	rf.xlog("当前日志%+v，commitIndex：%v", rf.logs.LogList, rf.commitIndex)
	// Your code here, if desired.
}

func (rf *Raft) killed() bool {
	z := atomic.LoadInt32(&rf.dead)
	return z == 1
}

const DEBUG = false

func (rf *Raft) xlog(desc string, v ...interface{}) {

	if DEBUG {
		s := "candidate"
		if rf.memberShip == FOLLOWER {
			s = "follower"
		} else if rf.memberShip == LEADER {
			s = "leader"
		}
		log.Printf(strconv.Itoa(GoID())+"-raft-"+strconv.Itoa(rf.me)+"-term-"+strconv.Itoa(rf.currentTerm)+"-"+s+"| "+desc+"\n", v...)
	}
}
func GoID() int {
	var buf [64]byte
	n := runtime.Stack(buf[:], false)
	// 得到id字符串
	idField := strings.Fields(strings.TrimPrefix(string(buf[:n]), "goroutine "))[0]
	id, err := strconv.Atoi(idField)
	if err != nil {
		panic(fmt.Sprintf("cannot get goroutine id: %v", err))
	}
	return id
}
